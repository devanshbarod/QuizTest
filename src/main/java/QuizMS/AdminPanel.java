    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        int row = table1.getSelectedRow();

    if (row != -1) {
        String name = table1.getValueAt(row, 1).toString(); // Assuming 'name' is in column 0
        t1.setText(dt.getValueAt(row, 1).toString());
        String name2=t1.getText();

        try {
            // Delete from database
            Connection con=ConnectionCode.getcon();
            PreparedStatement ps = con.prepareStatement("DELETE FROM registration WHERE username='"+name2+"'");
            PreparedStatement ps2 = con.prepareStatement("DELETE FROM login WHERE username='"+name2+"'");
            ps.executeUpdate();
            ps2.executeUpdate();

            // Delete from JTable
            DefaultTableModel model = (DefaultTableModel) table1.getModel();
            model.removeRow(row);

            JOptionPane.showMessageDialog(null, "Deleted successfully.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(null, "Please select a row to delete.");
    }

    }//GEN-LAST:event_jButton3ActionPerformed

